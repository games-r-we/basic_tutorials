// this includes the iostream library. this allows for the user to use cin or cout to input or output basic data
// # means an include
#include <iostream>

// this uses the name space std for standard libray. this allows the user to use system functions.
using namespace std;

// main() is where program execution begins.
// this is a function
int main() {
  //this method/function will execute in  linear fashion.

   // cout outputs a line of text.  \n means new line. 
   // as an alternative you could use cout << "Hello World" << endl ;
   cout << "Hello World \n";
   // this will print Hello World to the console and then it will start a new line


   //because the main type is int  (int main() ) must return an integer.
   return 0;
}
